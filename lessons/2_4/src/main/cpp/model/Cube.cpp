void Graphic::Cube::render(Camera &camera, std::list<std::shared_ptr<Light>> lights, glm::vec3 position){

    glUseProgram(shaderProgram);
    glUniform1i(glGetUniformLocation(shaderProgram, "material.diffuse"), 0);
    glUniform1i(glGetUniformLocation(shaderProgram, "material.specular"), 1);

    glm::mat4 view, projection;
    view = camera.GetViewMatrix();
    GLfloat width = 1024.0f;
    GLfloat height = 768.0f;
    GLfloat range = 1000.0f;
    projection = glm::perspective(45.0f, width / height, 0.1f, range);

    glUniformMatrix4fv(glGetUniformLocation(shaderProgram, "view"), 1, GL_FALSE, glm::value_ptr(view));
    glUniformMatrix4fv(glGetUniformLocation(shaderProgram, "projection"), 1, GL_FALSE, glm::value_ptr(projection));

    auto light = lights.front().get();//get first


    glUniform3f(glGetUniformLocation(shaderProgram, "viewPos"),
                camera.Position.x,
                camera.Position.y,
                camera.Position.z);

    glUniform3f(glGetUniformLocation(shaderProgram, "material.specular"), 0.5f, 0.5f, 0.5f);
    glUniform1f(glGetUniformLocation(shaderProgram, "material.shininess"), 32.0f);

    glUniform3f(glGetUniformLocation(shaderProgram, "light.position"), light->position.x, light->position.y, light->position.z);
    glUniform3f(glGetUniformLocation(shaderProgram, "light.ambient"), light->color.x, light->color.y, light->color.z);
    glUniform3f(glGetUniformLocation(shaderProgram, "light.diffuse"),  0.5f, 0.5f, 0.5f); // darken the light a bit to fit the scene
    glUniform3f(glGetUniformLocation(shaderProgram, "light.specular"), 1.0f, 1.0f, 1.0f);

    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, texture);

    glActiveTexture(GL_TEXTURE1);
    glBindTexture(GL_TEXTURE_2D, specularTexture);

    glBindVertexArray(VAO);
    glm::mat4 model;

    model = glm::translate(model, position);
    model = glm::scale(model, glm::vec3(side, side, side));
    glUniformMatrix4fv(glGetUniformLocation(shaderProgram, "model"), 1, GL_FALSE, glm::value_ptr(model));

    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);

};